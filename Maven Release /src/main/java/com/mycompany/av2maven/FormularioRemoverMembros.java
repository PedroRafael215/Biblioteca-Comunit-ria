    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nome =  (String) jComboBoxMembros.getSelectedItem(); 
    
    String DELETAR = "DELETE FROM Membros WHERE nome = ?";
       
       try{
           Connection conn = Conectar();
            PreparedStatement deletar = conn.prepareStatement(DELETAR);
           deletar.setString(1, nome);
            int res = deletar.executeUpdate();
            if(res > 0){
                          JOptionPane.showMessageDialog(null, "Membro deletado com Sucesso!");
                          Conexao.membros.clear();
                           Conexao.ImportarMembros();
                           
                          this.dispose();
            }
            
       }catch(HeadlessException e){
            JOptionPane.showMessageDialog(null, "Erro ao deletar Membro\nContacte o administrador do Sistema.");
       } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao deletar Membro\nContacte o administrador do Sistema.");
        }



// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

